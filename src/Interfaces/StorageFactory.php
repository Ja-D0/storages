<?php

namespace JaD0\Storages\Interfaces;

/**
 * Контракт фабрики, скрывающей создание конкретного хранилища за строковым идентификатором.
 * Смысл идентификатора определяет реализация фабрики.
 */
interface StorageFactory
{
    /**
     * Создает экземпляр хранилища по идентификатору.
     *
     * Физические фабрики используют имя бакета, контейнера или поддиректории.
     * Составные фабрики могут использовать логическое имя хранилища.
     *
     * @param string $identifier Идентификатор, поддерживаемый конкретной реализацией
     * @return Storage
     */
    public function createStorage(string $identifier): Storage;
}
